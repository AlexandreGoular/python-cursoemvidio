# Crie um programa que leia dois numeros e mostre a soma entre eles

# Entrada
numeroUm = int(input("Digite o primeiro numero: "))
numeroDois = int(input("Digite o segundo numero: "))

# Processamento
somaNumeros = numeroUm + numeroDois

# Saida
print("{} + {} = {}".format(numeroUm, numeroDois, somaNumeros))